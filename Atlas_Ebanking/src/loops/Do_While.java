package loops;

public class Do_While {

	public static void main(String[] args) 
	{
		
		/*
		 * Do-While:-->
		 * 		Do-while is similar to while loop only diff do-while
		 * 		accept iteration once even condition was return true.
		 */
		
		
		
		int i=0;
		do {
			System.out.println(i);
			i=i+1;
		} while (i<20);
		
		

	}

}
