package loops;

public class Foreach_loop {

	public static void main(String[] args) 
	{
		/*
		 * Foreach:-->
		 * 			Loop read array next values.

		 * Syntax:-->
		 * 				for(Variable var: Array)
		 * 				{
		 * 
		 * 				}
		 */
		
		String browser[]={"firefox","chrome","ie","safari","opera"};
		
		//Example Print all browser using foreach loop
		for (String eachbrowser : browser) 
		{
			System.out.println(eachbrowser);
		}
		
		
		
		
		
		
		
		
		

	}

}
